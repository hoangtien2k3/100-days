int *swap_array (int arr[], int n, int brr[]) {
    int m;
    int i = m;
    for (int i=0; i<n ; i++) {
        if (arr[i] = m) {
            arr[i] = brr[i];
        }
        else {
            brr[i] = -1;
        }
    }
}

